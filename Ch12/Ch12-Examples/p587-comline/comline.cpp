// comline.cpp
// Демонстрация работы с аргументами командной строки
#include <iostream>
using namespace std;

int main(int argc, char* argv[] )
  {
  cout << "\nargc = " << argc << endl; // число аргументов 

  for(int j=0; j<argc; j++)            // вывести аргументы
    cout << "Argument " << j << " = " << argv[j] << endl;
  return 0;
  }
