// массив строк
#include <iostream>
using namespace std;
///////////////////////////////////////////////////////////
int main ( )
{
	const int DAYS = 7; // количество строк в массиве
	const int MAX = 12; // максимальная длина каждой из них

	// массив строк
	char star [ DAYS ][ MAX ] =
	{
		"Monday", "Tuesday", "Wednesday", "Thursday",
		"Fryday", "Saturday", "Sunday"
	};

	// вывод всех строк на экран
	for ( int j = 0; j < DAYS; j++ )
		cout << star [ j ] << endl;

	return 0;
}


